# Condicionais 2

cor = input("Digite sua cor favorita: ").strip().lower()        

if cor == "amarela" or cor == "amarelo":
    print("Você gosta de esperança !")
elif cor == "verde":
    print("Você gosta de natureza!")
elif cor == "azul":
    print("Você gosta do céu!")
elif cor == "vermelho":
    print("Você gosta de paixão!")
else:
    print("Cor não reconhecida, mas é bonita!")

